NOMBRECLI CLIENTES.NOMBRECLIENTE%TYPE;
CIUDADCLI CLIENTES.CIUDAD%TYPE;
PAISCLI CLIENTES.PAIS%TYPE;
BEGIN
SELECT NOMBRECLIENTE,CIUDAD,PAIS INTO NOMBRECLI,CIUDADCLI,PAISCLI FROM CLIENTES WHERE CODIGOCLIENTE=CO;
